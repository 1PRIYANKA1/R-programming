//Write a C++ program to interchange values of two integer numbers. (Use call by reference)

#include <iostream>
using namespace std;
class Interchange 
{
    public:
        void swap(int &x, int &y) 
		{
            int temp = x;
            x = y;
            y = temp;
        }
};

int main() 
{
    int a, b;
    cout << "Enter two numbers: ";
    cin >> a >> b;
    Interchange obj;
    obj.swap(a, b);
    cout << "After swapping, the numbers are " << a << " and " << b << endl;
    return 0;
}













































































































































































































































































































